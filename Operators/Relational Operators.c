// Relational Operators

#include <stdio.h>
int main() {
    printf("10>8 :%5d\n", 10>8);
    printf("10<5 :%5d\n", 10<5);
    printf("10!=10 :%5d\n", 10!=10);
    printf("10==10 :%5d\n", 10==10);
    printf("10>=10 :%5d\n", 10>=10);
    printf("10<=30 :%5d\n", 10<=30);
}

/* Output:
10>8 : 1
10<5 : 0
10!=10 : 0
10==10 : 1
10>=10 : 1
10<=30 : 1
*/